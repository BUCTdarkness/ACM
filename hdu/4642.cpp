#include<iostream>
#include<algorithm>
#include<cstdio>
#include<cstring>
#include<cmath>
#include<string>
using namespace std;
int main()
{
    int T;
    while(cin>>T)
    {
        while(T--)
        {
            int n,m,x;
            cin>>n>>m;
            for(int i=0;i<n;i++)
                for(int j=0;j<m;j++)
                {
                    scanf("%d",&x);
                }
            if(x)
                cout<<"Alice"<<endl;
            else
                cout<<"Bob"<<endl;
        }
    }
    return 0;
}
